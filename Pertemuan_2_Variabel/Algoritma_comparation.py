a = 12
b = 12
# check apakah nilai A lebih besar dari B
hasil = a > b
print(f"Apakah nilai {a} lebih besar dari (>) nilai {b} adalah {hasil}")
# check apakah nilai A lebih besar sama dengan dari B
hasil = a >= b
print(f"Apakah nilai {a} lebih besar dari sama dengan (>=) nilai {b} adalah {hasil}")

# check apakah nilai A lebih kecil dari B
hasil = a < b
print(f"Apakah nilai {a} lebih kecil dari (<) nilai {b} adalah {hasil}")
# check apakah nilai A lebih kecil sama dengan dari B
hasil = a <= b
print(f"Apakah nilai {a} lebih kecil dari sama dengan (<=) nilai {b} adalah {hasil}")

a = 10 #tipe data nya integer
b = 10 #tipe datanya string atau text
# check apakah nilai A sama dengan nilai B
hasil = a == b
print(f"Apakah nilai dari {a} itu sama dengan (==) nilai {b} adalah {hasil}")

status = True
hasil = (status != False)
print(f"Apakah nilai setatus tidak (!) sama dengan (!=) False adalah {hasil}")

